/**
 * is_prime_number - Function that return 1 if the integer is a prime number.
 * @n: Element.
 * Return: 1 If the input integer is a prime number, otherwise return 0.
 */
int is_prime_number(int n)
{

	if (n > 1)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
