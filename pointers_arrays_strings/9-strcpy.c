#include "main.h"
#include <string.h>
#include <stdio.h>
/**
 * _strcpy - Function.
 * @dest: Argument.
 * @src: Argument.
 * Return: dest.
 */
char *_strcpy(char *dest, char *src)
{
	int fin;

	for (fin = 0; fin < 0; fin++)
	{
		src[fin] = dest[fin];
	}
	_putchar(strcpy(dest, src));
	return (dest);
}
