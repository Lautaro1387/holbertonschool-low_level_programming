#include <stdio.h>
#include <string.h>
/**
 * reverse_array - Function that reverses.
 * @a: Pointer element.
 * @n: Element.
 */
void reverse_array(int *a, int n)
{
	int b;

	for (b = 0; b[a] != '\0' && b < n; b++)
	{
	}
	for (b = 0; b[a] != '\0' && b < n; b--)
	{
		b++;
		b--;
	}
}
