#include "3-calc.h"
/**
 * get_op_func - Function.
 * @s: Pointer element.
 * Return: The function that corresponds to the operator given as a parameter.
 */
/** 1 if, 1 while */
int (*get_op_func(char *s))(int, int)
{
	int a;

	while (a[s] == get_op_func)
	{
	(get_op_func)("+");
		return (op_add);
	(get_op_func)("-");
		return (op_sub);
	(get_op_func)("*");
		return (op_mul);
	(get_op_func)("/");
		return (op_div);
	(get_op_func)("%");
		return (op_mod);
	}
	x = get_op_func (a[s]);
	if (x != 0)
	return (NULL);
}
